// Arithmetic operators
var x = 12, y = 13;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y); // Full division rather than returning the quotient
console.log(y / x);
console.log(x % y); // Returns remainder
console.log(y % x);





